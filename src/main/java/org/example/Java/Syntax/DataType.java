package org.example.Java.Syntax;

public class DataType {
    //Java Data Types
    /*Primitive Data Types: byte, short, int, long,
     float, double, boolean, char
     */

    //Non-Primitive Data Types: String, Array, Class

    //Primitive -> Numeric: Character, Integral
    //Integral -> Integer: byte, short, int, long
    //Integral -> Floating-point: float, double

    boolean myBoolean; //Default (value is false, size is 1 bit)
    byte myByte; //Default (value is 0, size is 8 bits)
    short myShort; //Default (value is 0, size is 16 bits)
    int myInt; //Default (value is 0, size is 32 bits)
    long myLong; //Default (value is 0, size is 64 bits)
    float myFloat; //Default (value is 0.0f, size is 32 bits)
    double myDouble; //Default (value is 0.0, size is 64 bits)
    char myChar; //Default (value is '\u0000', size is 16 bits)

    String myString; //Default (value is null, size is 16 bits)
}
